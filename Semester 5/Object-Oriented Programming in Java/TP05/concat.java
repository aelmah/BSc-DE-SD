public class concat {
    // Cette classe s'appelle "concat", ce qui suggère qu'elle a une méthode pour concaténer des chaînes de caractères.

    public String concat(String s1, String s2) {
        // Cette méthode "concat" prend deux chaînes de caractères (s1 et s2) en entrée et retourne une chaîne de caractères.

        return s1 + s2;
        // Elle concatène les deux chaînes s1 et s2 en utilisant l'opérateur "+" et renvoie le résultat.
    }
}
